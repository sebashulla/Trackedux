export const getExamTemplateByCode = (code) => EXAM_TEMPLATES.find((template) => template.code === code)

export const getTemplateFormDefaults = (template) => ({
  name: template.defaultName,
  targetDate: '',
  ...(template.fields ?? []).reduce((fields, field) => ({
    ...fields,
    [field.name]: field.defaultValue ?? '',
  }), {}),
})

export const flattenTemplateTopicRows = (topics) => (
  topics.flatMap((topic) => {
    const baseTopic = {
      name: topic.name,
      code: topic.code,
      importance: topic.importance,
      notes: topic.notes,
    }

    if (!topic.children?.length) return [baseTopic]

    return [
      baseTopic,
      ...topic.children.map((child, index) => {
        const childTopic = typeof child === 'string' ? { name: child } : child

        return {
          name: `${topic.name}: ${childTopic.name}`,
          code: childTopic.code ?? (topic.code ? `${topic.code}.${index + 1}` : undefined),
          importance: childTopic.importance ?? topic.importance,
          notes: childTopic.notes,
        }
      }),
    ]
  })
)

export const flattenTemplateTopics = (topics) => flattenTemplateTopicRows(topics).map((topic) => topic.name)

export const countTemplateTopics = (template) => (
  template.courses.reduce((total, course) => total + flattenTemplateTopics(course.topics).length, 0)
)
